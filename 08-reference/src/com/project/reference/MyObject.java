package com.project.reference;

public class MyObject {
  Integer num;

  public MyObject(Integer num) {
    this.num = num;
  }

  public Integer getNum() {
    return this.num;
  }

  public void setNum(Integer num) {
    this.num = num;
  }

  public String toString() {
    return this.num.toString();
  }

}
